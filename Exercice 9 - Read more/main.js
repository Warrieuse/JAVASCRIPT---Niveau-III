let vDivShort = document.getElementById("div_short");
let vDivMore = document.getElementById("div_More");


function fReadMore() {
    if (vDivShort.display="block"){  
            vDivShort.style.display = "none";
            vDivMore.style.display = "block";
     } else {
             vDivShort.style.display = "block";
            vDivMore.style.display = "none";
     }}; 

